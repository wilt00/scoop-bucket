A personal, unofficial [scoop](https://scoop.sh) bucket of miscellaneous Windows applications.

## Usage

``````powershell
scoop bucket add wilt00 "https://github.com/wilt00/scoop-bucket"
scoop install wilt00/<app_name>
``````

